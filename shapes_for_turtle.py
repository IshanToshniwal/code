import turtle
s = turtle.Screen()
s.bgcolor('dark green')
t = turtle.Turtle()
t.width(3)
t.begin_fill()
for i in range(1, 4):
    t.forward(200)
    t.right(90)
t.end_fill()
turtle.done()


